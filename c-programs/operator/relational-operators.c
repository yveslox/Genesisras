#include<stdio.h>

int main()
{
 int n;
 printf("Enter any Number:\n");
 scanf("%d",&n);
 
 printf("(n < 10) is  = %d\n",(n < 10));
 printf("(n > 10) is  = %d\n",(n > 10));
 printf("(n <= 10) is  = %d\n",(n <= 10));
 printf("(n >= 10) is  = %d\n",(n >= 10));
 printf("(n == 10) is  = %d\n",(n == 10));
 printf("(n != 10) is  = %d\n",(n != 10));
 
}
