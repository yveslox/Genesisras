import java.io.*;
import java.util.Scanner;

class if_ex2  
{      
    public static void main(String args[])
    { 
        int a = 10;
        int b = 20;

        if(a>b)
        {
            System.out.println("a is greater than b");
        }

        if(a<b)
        {
            System.out.println("a is less than b");
        }

        if(a==b)
        {
            System.out.println("a is equal to b");
        }
    }
}