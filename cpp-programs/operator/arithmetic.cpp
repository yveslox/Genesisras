#include<bits/stdc++.h>
using namespace std;

int main()
{
 int a,b,res;
 
 cout<<"Enter two numbers :::";
 cin>>a>>b;
 
 res = a + b;
 cout<<a<<" + "<<b<<" = "<<res<<endl;
 
 res = a - b;
 cout<<a<<" - "<<b<<" = "<<res<<endl;
 
 res = a * b;
 cout<<a<<" * "<<b<<" = "<<res<<endl;

 res = a / b;
 cout<<a<<" / "<<b<<" = "<<res<<endl;
 
 res = a % b;
 cout<<a<<" % "<<b<<" = "<<res<<endl;

}
