#include<bits/stdc++.h>
using namespace std;

int main()
{
 int n;
 
 cout<<"Enter any number :";
 cin>>n;
 
 cout<<"(n < 10) is = "<<(n<10)<<endl;
 cout<<"(n > 10) is = "<<(n>10)<<endl;
 cout<<"(n <= 10) is = "<<(n<=10)<<endl;
 cout<<"(n >= 10) is = "<<(n>=10)<<endl;
 cout<<"(n == 10) is = "<<(n==10)<<endl;
 cout<<"(n != 10) is = "<<(n!=10)<<endl;

}
