#include<bits/stdc++.h>
using namespace std;

int main()
{
 cout<<"Logical AND (&&) Operator."<<endl;
 cout<<"((10<20) && (30 < 40)): "<<((10<20) && (30 < 40))<<endl;
 cout<<"((10<20) && (30 > 40)): "<<((10<20) && (30 > 40))<<endl;
 cout<<"((10>20) && (30 < 40)): "<<((10>20) && (30 < 40))<<endl;
 cout<<"((10>20) && (30 > 40)): "<<((10>20) && (30 > 40))<<endl;
 
 
 cout<<"Logical OR (||) Operator."<<endl;
 cout<<"((10<20) || (30 < 40)): "<<((10<20) || (30 < 40))<<endl;
 cout<<"((10<20) || (30 > 40)): "<<((10<20) || (30 > 40))<<endl;
 cout<<"((10>20) || (30 < 40)): "<<((10>20) || (30 < 40))<<endl;
 cout<<"((10>20) || (30 > 40)): "<<((10>20) || (30 > 40))<<endl;
  
 cout<<"Logical NOT (!) Operator."<<endl;
 cout<<" (10<20) "<< !(10<20)<<endl;
 cout<<" (10>20) "<< !(10>20)<<endl;

}
