using System;
namespace data_types{
    class Program{
        static void Main(string[] args){
            int num = 12;
            Console.WriteLine("num : {0}",num);

            char ch = 'z';
            Console.WriteLine("ch : {0}",ch);

            float fl = 15.78f;
            Console.WriteLine("fl : {0}",fl);

            string name ="john";
            Console.WriteLine("name : {0}",name);

            bool val = true;
            Console.WriteLine("val : {0}",val);
        }
    }
}