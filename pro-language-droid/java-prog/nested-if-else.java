class nested_if_else 
{
    public static void main(String args[])
    {
        int x = 10;
        if(x==10)
        {
            if(x>5)
            {
              System.out.println("x is 10 and is greater than 5");
            }
        }   
        else 
        {
            System.out.println("x is not 10");
        }
    }
   
}
